std::string Version::GetRandomAsciiArt() {
    srand((unsigned int)time(NULL));
    
    if (useUTF8Art) {
        int index = rand() % asciiArtCountUTF8;  // 使用 UTF8 数组大小
        return std::string(asciiArtsUTF8[index]);
    } else {
        int index = rand() % asciiArtCountCP437; // 使用 CP437 数组大小
        return std::string(asciiArtsCP437[index]);
    }
}

